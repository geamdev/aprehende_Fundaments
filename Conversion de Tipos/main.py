# A lo largo de la historia de los lenguajes de programación se han definido una variedad de tipos de datos para llegar a ser precisos en el la función principal de un programa, que es el procesamiento de información. En python los tipos de datos principales son:

#Numéricos
##Entero ( int )
numero = 15
print(type(numero))
##Decimal ( float )
numero = 26.8
print(type(numero))
##Complejos ( complex )
numero = complex(25,-8)
print(type(numero))
#Booleanos ( bool )
booleano = True
print(type(booleano))
booleano = False
print(type(booleano))
#Texto
##Cadena de texto ( str )
texto = 'soy un texto'
print(type(texto))

